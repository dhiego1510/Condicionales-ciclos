// Ingresar  por  pantalla  un  número  entre  1  y  12,  luego  imprimir  en  letras  el  mes  al que  corresponde  el  valor  ingresado  y  los  signos  del  zodiaco.  Ejemplo:  si  el  valor ingresado  es  5,  entonces  imprimir  “Mayo”,  “Tauro  -  Géminis”.

let Nmes = Number(prompt("ingresa numero mes"));

Nmes===1 ? alert("Mes Enero, signo Capricornio o acuario") : false;
Nmes===2 ? alert("Mes Febrero, signo acuario o piscis") : false;
Nmes===3 ? alert("Mes Marzo, signo piscis o aries") : false;
Nmes===4 ? alert("Mes Abril, signo aries o tauro") : false;
Nmes===5 ? alert("Mes Mayo, signo tauro o geminis") : false;
Nmes===6 ? alert("Mes Junio, signo geminis o cancer") : false;
Nmes===7 ? alert("Mes Julio, signo cancer o leo") : false;
Nmes===8 ? alert("Mes Agosto, signo leo o virgo") : false;
Nmes===9 ? alert("Mes Septiembre, signo virgo o libra") : false;
Nmes===10 ? alert("Mes Octubre, signo libra o escorpio") : false;
Nmes===11? alert("Mes Noviembre, signo escorpio o sagitario") : false;
Nmes===12? alert("Mes Diciembre, signo sagitario o capricornio") : false;
